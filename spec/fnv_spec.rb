require "spec_helper"

describe River::FNV do
  describe "#fnv1_hash" do
    it "hashes strings to 32 bits" do
      TEST_STRS.each do |test_str|
        expect(River::FNV.fnv1_hash(test_str, size: 32)).to eq(FNV1_32_HASHES[test_str])
      end
    end

    it "hashes strings to 64 bits" do
      TEST_STRS.each do |test_str|
        expect(River::FNV.fnv1_hash(test_str, size: 64)).to eq(FNV1_64_HASHES[test_str])
      end
    end
  end

  # Just a sanity check to make sure the number of 0xffs is right.
  describe "MASK" do
    it "contains masks equal to integer limits" do
      expect(River::FNV.const_get(:MASK)[32]).to eq(4_294_967_295)
      expect(River::FNV.const_get(:MASK)[64]).to eq(18_446_744_073_709_551_615)
    end
  end
end

